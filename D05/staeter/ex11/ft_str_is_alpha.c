int ft_str_is_alpha(char *str)
{
	if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
		return (ft_str_is_alpha(++str));
	else if (*str == 0)
		return (1);
	else
		return (0);
}
