char	*ft_strncat(char *dest, char *src, int nb)
{
	char *tmp;

	tmp = dest;
	while (*tmp != 0)
		tmp++;
	while (*src != 0 && nb > 0)
	{
		*tmp = *src;
		tmp++;
		src++;
		nb--;
	}
	*tmp = 0;
	return (dest);
}
